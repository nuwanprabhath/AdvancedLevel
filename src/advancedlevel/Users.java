    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            // adds the new user to the system
            SessionFactory sessionFactory = NewHibernateUtil.getSessionFactory();
            Session session = sessionFactory.openSession();
            session.beginTransaction();

            String username = jTextField1.getText();
            if (username.equals("")) {
                jLabel4.setText("Invalid username");
                return;
            }
            User user = new User(username, UserPasswordMatch.getHash(jTextField2.getText()), (String) jComboBox1.getSelectedItem());
            //           UserPasswordMatch.changePassword(user, "sdf", (String)jComboBox1.getSelectedItem());
            try {
                session.save(user);
                session.getTransaction().commit();
                session.close();
                jLabel4.setText("User added");
                jTextField1.setText("");
                jTextField2.setText("");
            } catch (Exception e) {
                jLabel4.setText("Duplicate username");
                jTextField1.setText("");
                jTextField2.setText("");
            }
        } catch (Exception ex) {
            Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

