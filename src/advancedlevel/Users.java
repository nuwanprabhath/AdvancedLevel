    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            SessionFactory sessionFactory = NewHibernateUtil.getSessionFactory();
            Session session = sessionFactory.openSession();
            session.beginTransaction();
            Query qr=session.createQuery("from User where username=:code");
            qr.setParameter("code",(String) this.jComboBox2.getSelectedItem());
            User user = (User)qr.list().get(0);
            
            user.setPasswordHash(UserPasswordMatch.getHash(jTextField3.getText()));
            session.update(user);
            session.getTransaction().commit();
            session.close();
            jLabel7.setText("Password successfully reset");
        } catch (Exception ex) {
            Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
